# Variable definitions
name = "Miguel"      # string
count = 1           # number

# While loop
while count <= 3:
    print("While loop count:", count)
    count += 1

# For loop
for num in range(1, 6):
    # If statement
    if num % 2 == 0:
        print(num, "is even")
    else:
        print(num, "is odd")
